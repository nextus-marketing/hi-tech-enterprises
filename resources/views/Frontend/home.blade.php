  const text = toggleBtn.querySelector('.btn-title');

  let expanded = false;

  toggleBtn.addEventListener('click', function (e) {
    e.preventDefault();

    expanded = !expanded;

    extraItems.forEach(item => {
      item.style.display = expanded ? 'flex' : 'none';
